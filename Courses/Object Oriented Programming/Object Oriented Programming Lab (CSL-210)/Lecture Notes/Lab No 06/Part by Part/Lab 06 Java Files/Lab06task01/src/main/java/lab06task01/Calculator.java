package lab06task01;

public class Calculator {

    public int sum(int num1, int num2) {
        return num1 + num2;
    }
    public int sum(int num1, int num2, int num3) {
        return num1 + num2 + num3;
    }
    public int sum(int num1, int num2, int num3, int num4) {
        return num1 + num2 + num3 + num4;
    }
}
